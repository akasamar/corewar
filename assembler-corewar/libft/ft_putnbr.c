#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	i;

	i = (unsigned int)n;
	if (n < 0)
	{
		write(1, "-", 1);
		i = -i;
	}
	if (i / 10)
		ft_putnbr(i / 10);
	i = i % 10 + 48;
	write(1, &i, 1);
}
