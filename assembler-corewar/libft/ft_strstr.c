#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int i;

	if (s2[0] == '\0')
		return ((char*)s1);
	while (*s1)
	{
		i = 0;
		while (*(s1 + i) == s2[i])
		{
			i++;
			if (s2[i] == '\0')
				return ((char*)s1);
		}
		s1++;
	}
	return (0);
}
