#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned long int	res;
	size_t				i;
	int					a;

	i = 0;
	res = 0;
	while (ft_isspace(str[i]))
		i++;
	a = (str[i] == '-') ? -1 : 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (((str[i] > 47) && (str[i] < 58)) && str[i])
	{
		res = res * 10 + str[i] - '0';
		if ((res > 922337203685477580 || (res == 922337203685477580
			&& (str[i] - '0') > 7)) && a == 1)
			return (-1);
		else if ((res > 922337203685477580 || (res == 922337203685477580
			&& (str[i] - '0') > 8)) && a == -1)
			return (0);
		i++;
	}
	return (a * (int)res);
}
