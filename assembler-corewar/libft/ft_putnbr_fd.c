#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	i;

	i = (unsigned int)n;
	if (n < 0)
	{
		write(fd, "-", 1);
		i = -i;
	}
	if (i / 10)
		ft_putnbr_fd(i / 10, fd);
	i = i % 10 + 48;
	write(fd, &i, 1);
}
